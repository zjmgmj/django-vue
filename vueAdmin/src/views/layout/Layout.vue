<template lang="pug">
div  
  div(:class="classObj" class="app-wrapper")
    Headerbar
    .drawer-bg(v-if="device==='mobile'&&sidebar.opened" class="drawer-bg" @click="handleClickOutside")    
    sidebar.sidebar-container(style="top:45px")
    .main-container.mt-45
      Navbar
      tags-view
      app-main      
</template>

<script>
import { Navbar, Sidebar, AppMain, Headerbar, TagsView } from './components'
import ResizeMixin from './mixin/ResizeHandler'

export default {
  name: 'Layout',
  components: {
    Navbar,
    Sidebar,
    AppMain,
    Headerbar,
    TagsView
  },
  mixins: [ResizeMixin],
  computed: {
    sidebar() {
      return this.$store.state.app.sidebar
    },
    device() {
      return this.$store.state.app.device
    },
    classObj() {
      return {
        hideSidebar: !this.sidebar.opened,
        openSidebar: this.sidebar.opened,
        withoutAnimation: this.sidebar.withoutAnimation,
        mobile: this.device === 'mobile'
      }
    }
  },
  methods: {
    handleClickOutside() {
      this.$store.dispatch('CloseSideBar', { withoutAnimation: false })
    }
  }
}
</script>

